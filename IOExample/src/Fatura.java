
public class Fatura {
	
	private  long tcNo;
	private String ad;
	private String soyad;
	private long hesapNo;
	private double tutar;
	private String yerlesimYeri;
	public long getTcNo() {
		return tcNo;
	}
	public void setTcNo(long tcNo) {
		this.tcNo = tcNo;
	}
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public String getSoyad() {
		return soyad;
	}
	public void setSoyad(String soyad) {
		this.soyad = soyad;
	}
	public long getHesapNo() {
		return hesapNo;
	}
	public void setHesapNo(long hesapNo) {
		this.hesapNo = hesapNo;
	}
	public double getTutar() {
		return tutar;
	}
	public void setTutar(double tutar) {
		this.tutar = tutar;
	}
	public String getYerlesimYeri() {
		return yerlesimYeri;
	}
	public void setYerlesimYeri(String yerlesimYeri) {
		this.yerlesimYeri = yerlesimYeri;
	}
	
	
	

	
}
